#include "3-calc.h"
/**
 * op_add - add nums
 * @a: fst num
 * @b: scnd num
 *
 * Return: added nums
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub nums
 * @a: fst num
 * @a: scnd num
 *
 * Return: sub num
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mm
 * @a: m
 * @b: m
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_mul - mm
 * @a: m
 * @b: m
 *
 * Return: mul
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mul - mm
 * @a: m
 * @b: m
 *
 * Return: mul
 */
int op_mod(int a, int b)
{
	return (a % b);
}
